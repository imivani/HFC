let sliderTimer;
let pendingPage;
let isNavigating = false;

function render(page = getPage()) {
  pendingPage = page;
  document.body.classList.add('page-changing', 'page-entering');
  document.body.classList.remove('page-exiting', 'menu-open');
  window.clearTimeout(sliderTimer);
  document.getElementById('root').innerHTML = `${header(page)}<main>${pages[page]()}</main>${footer()}`;
  bindInteractions();
  requestAnimationFrame(() => {
    requestAnimationFrame(() => document.body.classList.remove('page-changing'));
  });
  window.setTimeout(() => document.body.classList.remove('page-entering'), 760);
  window.scrollTo({ top: 0, behavior: 'smooth' });
  isNavigating = false;
}

function bindInteractions() {
  const nav = document.querySelector('.nav');
  const menuButton = document.querySelector('.menu-button');
  menuButton?.addEventListener('click', () => {
    document.body.classList.toggle('menu-open');
    nav.classList.toggle('open');
  });
  nav?.querySelectorAll('a').forEach((link) => link.addEventListener('click', () => document.body.classList.remove('menu-open')));
  document.querySelectorAll('a[href^="#/"]').forEach((link) => {
    link.addEventListener('click', handleInternalNavigation);
  });

  const form = document.querySelector('.contact-form');
  form?.addEventListener('submit', (event) => {
    event.preventDefault();
    form.querySelector('.success-message').hidden = false;
  });

  bindSlider();
  bindReveal();
  updateHeaderState();
}

function handleInternalNavigation(event) {
  const url = new URL(event.currentTarget.href, window.location.href);
  const nextPage = url.hash.replace('#/', '') || 'home';
  if (isNavigating) {
    event.preventDefault();
    return;
  }
  if (!pages[nextPage] || nextPage === getPage()) return;

  event.preventDefault();
  isNavigating = true;
  document.body.classList.remove('menu-open');
  document.querySelector('.nav')?.classList.remove('open');
  document.body.classList.add('page-exiting');
  window.clearTimeout(sliderTimer);

  window.setTimeout(() => {
    pendingPage = nextPage;
    window.location.hash = `#/${nextPage}`;
  }, 260);
}

function bindSlider() {
  const slider = document.querySelector('[data-slider]');
  if (!slider) return;

  const slides = [...slider.querySelectorAll('[data-slide]')];
  const dots = [...slider.querySelectorAll('[data-dot]')];
  let active = 0;

  const go = (next) => {
    active = (next + slides.length) % slides.length;
    slides.forEach((slide, index) => slide.classList.toggle('active', index === active));
    dots.forEach((dot, index) => dot.classList.toggle('active', index === active));
  };

  slider.querySelectorAll('[data-dir]').forEach((button) => {
    button.addEventListener('click', () => go(active + Number(button.dataset.dir)));
  });
  dots.forEach((dot) => dot.addEventListener('click', () => go(Number(dot.dataset.dot))));
  sliderTimer = window.setInterval(() => go(active + 1), 7200);
}

function bindReveal() {
  const items = [...document.querySelectorAll('.reveal')];
  const observer = new IntersectionObserver((entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add('is-visible');
        observer.unobserve(entry.target);
      }
    });
  }, { rootMargin: '0px 0px -8% 0px', threshold: 0.01 });

  items.forEach((item) => observer.observe(item));
}

function updateHeaderState() {
  document.body.classList.toggle('is-scrolled', window.scrollY > 16);
  document.documentElement.style.setProperty('--scroll-progress', `${Math.min(1, window.scrollY / 720)}`);
}

window.addEventListener('hashchange', () => render());
window.addEventListener('scroll', updateHeaderState, { passive: true });
render();
