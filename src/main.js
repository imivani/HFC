function prefersReducedMotion() {
  return window.matchMedia('(prefers-reduced-motion: reduce)').matches;
}

function render(page = getPage()) {
  const reducedMotion = prefersReducedMotion();
  pendingPage = page;
  document.body.classList.add('page-changing', 'page-entering');
  document.body.classList.remove('page-exiting', 'menu-open');
  document.body.classList.add('has-mobile-cta');
  window.clearTimeout(sliderTimer);
  updatePageMeta(page);
  document.getElementById('root').innerHTML = `${header(page)}${sectionNav(page)}<main>${pages[page]()}</main>${footer()}${mobileStickyCta(page)}`;
  bindInteractions();

  const finishEnter = () => document.body.classList.remove('page-changing');
  if (reducedMotion) {
    finishEnter();
  } else {
    requestAnimationFrame(() => {
      requestAnimationFrame(finishEnter);
    });
  }

  window.setTimeout(() => document.body.classList.remove('page-entering'), reducedMotion ? 0 : PAGE_ENTER_MS);
  window.scrollTo({ top: 0, behavior: reducedMotion ? 'auto' : 'smooth' });
  isNavigating = false;
}

function updatePageMeta(page) {
  const [title, description] = pageMeta[page] || pageMeta.home;
  document.title = title;

  document.querySelector('meta[name="description"]')?.setAttribute('content', description);
  document.querySelector('meta[property="og:title"]')?.setAttribute('content', title);
  document.querySelector('meta[property="og:description"]')?.setAttribute('content', description);
  document.querySelector('meta[name="twitter:title"]')?.setAttribute('content', title);
  document.querySelector('meta[name="twitter:description"]')?.setAttribute('content', description);
}

function bindInteractions() {
  const nav = document.querySelector('.nav');
  const menuButton = document.querySelector('.menu-button');
  const closeMenu = () => {
    document.body.classList.remove('menu-open');
    nav?.classList.remove('open');
    menuButton?.setAttribute('aria-expanded', 'false');
  };

  menuButton?.setAttribute('aria-expanded', 'false');
  menuButton?.addEventListener('click', () => {
    const isOpen = document.body.classList.toggle('menu-open');
    nav?.classList.toggle('open', isOpen);
    menuButton.setAttribute('aria-expanded', String(isOpen));
  });
  nav?.querySelectorAll('a').forEach((link) => link.addEventListener('click', closeMenu));
  document.querySelectorAll('a[href^="#/"]').forEach((link) => {
    link.addEventListener('click', handleInternalNavigation);
  });

  bindSlider();
  bindSectionNav();
  bindReportFilters();
  bindPlacementFilters();
  bindMailtoForms();
  bindReveal();
  updateHeaderState();
}

function handleInternalNavigation(event) {
  const url = new URL(event.currentTarget.href, window.location.href);
  const nextPage = url.hash.replace('#/', '') || 'home';
  if (isNavigating) {
    event.preventDefault();
    return;
  }
  if (!pages[nextPage] || nextPage === getPage()) return;

  event.preventDefault();
  isNavigating = true;
  document.body.classList.remove('menu-open');
  document.querySelector('.nav')?.classList.remove('open');
  document.querySelector('.menu-button')?.setAttribute('aria-expanded', 'false');
  document.body.classList.add('page-exiting');
  window.clearTimeout(sliderTimer);

  window.setTimeout(() => {
    pendingPage = nextPage;
    window.location.hash = `#/${nextPage}`;
  }, prefersReducedMotion() ? 0 : PAGE_EXIT_MS);
}

function bindSlider() {
  const slider = document.querySelector('[data-slider]');
  if (!slider) return;

  const slides = [...slider.querySelectorAll('[data-slide]')];
  const dots = [...slider.querySelectorAll('[data-dot]')];
  let active = 0;

  const go = (next) => {
    active = (next + slides.length) % slides.length;
    slides.forEach((slide, index) => slide.classList.toggle('active', index === active));
    dots.forEach((dot, index) => {
      const isActive = index === active;
      dot.classList.toggle('active', isActive);
      dot.setAttribute('aria-pressed', String(isActive));
    });
  };

  dots.forEach((dot, index) => dot.setAttribute('aria-pressed', String(index === active)));

  slider.querySelectorAll('[data-dir]').forEach((button) => {
    button.addEventListener('click', () => go(active + Number(button.dataset.dir)));
  });
  dots.forEach((dot) => dot.addEventListener('click', () => go(Number(dot.dataset.dot))));
  if (!prefersReducedMotion()) {
    sliderTimer = window.setInterval(() => go(active + 1), 7200);
  }
}

function bindSectionNav() {
  if (sectionScrollHandler) {
    window.removeEventListener('scroll', sectionScrollHandler);
    sectionScrollHandler = undefined;
  }

  const buttons = [...document.querySelectorAll('[data-section-target]')];
  if (!buttons.length) return;

  const setActive = (id) => {
    buttons.forEach((button) => {
      const isActive = button.dataset.sectionTarget === id;
      button.classList.toggle('active', isActive);
      button.setAttribute('aria-current', String(isActive));
    });
  };

  buttons.forEach((button) => {
    button.addEventListener('click', () => {
      const target = document.getElementById(button.dataset.sectionTarget);
      if (!target) return;
      setActive(button.dataset.sectionTarget);
      target.scrollIntoView({ behavior: prefersReducedMotion() ? 'auto' : 'smooth', block: 'start' });
    });
  });

  const sections = buttons.map((button) => document.getElementById(button.dataset.sectionTarget)).filter(Boolean);
  let sectionTicking = false;
  const updateActiveSection = () => {
    const anchor = Math.min(window.innerHeight * 0.42, 360);
    const current = sections.reduce((active, section) => (
      section.getBoundingClientRect().top <= anchor ? section : active
    ), sections[0]);
    setActive(current.id);
  };

  sectionScrollHandler = () => {
    if (sectionTicking) return;
    sectionTicking = true;
    requestAnimationFrame(() => {
      updateActiveSection();
      sectionTicking = false;
    });
  };

  window.addEventListener('scroll', sectionScrollHandler, { passive: true });
  updateActiveSection();
}

function bindReportFilters() {
  const form = document.querySelector('.report-tools');
  if (!form) return;

  const searchInput = form.querySelector('[data-report-search]');
  const yearSelect = form.querySelector('[data-report-year]');
  const countEl = form.querySelector('[data-report-count]');
  const cards = [...document.querySelectorAll('[data-report-card]')];
  const groups = [...document.querySelectorAll('[data-report-year-group]')];

  const applyFilters = () => {
    const query = (searchInput?.value || '').trim().toLowerCase();
    const year = yearSelect?.value || '';
    let visibleCount = 0;

    cards.forEach((card) => {
      const matchesYear = !year || card.dataset.year === year;
      const matchesQuery = !query || (card.dataset.search || '').includes(query);
      const isVisible = matchesYear && matchesQuery;
      card.hidden = !isVisible;
      if (isVisible) visibleCount += 1;
    });

    groups.forEach((group) => {
      const visibleCards = [...group.querySelectorAll('[data-report-card]')].filter((card) => !card.hidden);
      group.hidden = visibleCards.length === 0;
      const summary = group.querySelector('.report-year-header span');
      if (summary) summary.textContent = `${visibleCards.length} ${visibleCards.length === 1 ? 'report' : 'reports'}`;
    });

    if (countEl) countEl.textContent = `${visibleCount} ${visibleCount === 1 ? 'report' : 'reports'} available`;
  };

  searchInput?.addEventListener('input', applyFilters);
  yearSelect?.addEventListener('change', applyFilters);
  applyFilters();
}

function bindPlacementFilters() {
  const buttons = [...document.querySelectorAll('[data-placement-filter]')];
  if (!buttons.length) return;

  const cards = [...document.querySelectorAll('[data-placement-card]')];
  const groups = [...document.querySelectorAll('.placement-category')];
  const countEl = document.querySelector('[data-placement-count]');

  const applyFilter = (filter) => {
    let visibleCount = 0;

    buttons.forEach((button) => {
      const isActive = button.dataset.placementFilter === filter;
      button.classList.toggle('active', isActive);
      button.setAttribute('aria-pressed', String(isActive));
    });

    cards.forEach((card) => {
      const categories = (card.dataset.placementCategory || '').split(' ');
      const isVisible = filter === 'all' || categories.includes(filter);
      card.hidden = !isVisible;
      if (isVisible) visibleCount += 1;
    });

    groups.forEach((group) => {
      const visibleCards = [...group.querySelectorAll('[data-placement-card]')].filter((card) => !card.hidden);
      group.hidden = visibleCards.length === 0;
    });

    if (countEl) countEl.textContent = `${visibleCount} ${visibleCount === 1 ? 'company' : 'companies'} shown`;
  };

  buttons.forEach((button) => {
    button.addEventListener('click', () => applyFilter(button.dataset.placementFilter));
  });
}

function bindMailtoForms() {
  document.querySelectorAll('[data-mailto-form]').forEach((form) => {
    form.addEventListener('submit', (event) => {
      event.preventDefault();
      if (!form.reportValidity()) return;

      const subject = form.dataset.mailtoSubject || 'HFC inquiry';
      const fields = [...new FormData(form).entries()]
        .filter(([, value]) => String(value).trim())
        .map(([key, value]) => `${key}: ${String(value).trim()}`);
      const body = `${fields.join('\n')}\n\nSource: Haskayne Finance Club website`;
      const mailto = `mailto:${contactEmail}?subject=${encodeURIComponent(subject)}&body=${encodeURIComponent(body)}`;
      const feedback = form.querySelector('.success-message');

      if (feedback) feedback.hidden = false;
      window.location.href = mailto;
    });
  });
}

function bindReveal() {
  applyRevealStagger();
  const items = [...document.querySelectorAll('.reveal')];
  const reveal = (item) => {
    item.classList.add('is-visible');
    window.setTimeout(() => item.classList.add('reveal-complete'), prefersReducedMotion() ? 0 : 960);
  };

  if (prefersReducedMotion() || !('IntersectionObserver' in window)) {
    items.forEach(reveal);
    return;
  }

  const observer = new IntersectionObserver((entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        reveal(entry.target);
        observer.unobserve(entry.target);
      }
    });
  }, { rootMargin: '0px 0px -8% 0px', threshold: 0.01 });

  items.forEach((item) => observer.observe(item));
}

function applyRevealStagger() {
  staggeredRevealSelectors.forEach((selector) => {
    document.querySelectorAll(selector).forEach((item, index) => {
      item.classList.add('reveal-item');
      item.style.setProperty('--reveal-index', `${Math.min(index, 8)}`);
    });
  });
}

function updateHeaderState() {
  document.body.classList.toggle('is-scrolled', window.scrollY > 16);
  document.documentElement.style.setProperty('--scroll-progress', `${Math.min(1, window.scrollY / 720)}`);
}

function requestHeaderUpdate() {
  if (scrollTicking) return;
  scrollTicking = true;
  requestAnimationFrame(() => {
    updateHeaderState();
    scrollTicking = false;
  });
}

window.addEventListener('hashchange', () => render());
